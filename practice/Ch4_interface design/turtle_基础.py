# -*- coding:UTF-8 -*-
# !/usr/bin/env python

import turtle

bob = turtle.Turtle()   # create an object
print(bob)

bob.fd(100)     # forward 100 pixels   画线
bob.lt(90)      # left turn 90 degrees 箭头方向
bob.bk(100)     # backward 100 pixels  画线
bob.rt(90)      # right turn 90 pixels 箭头方向

